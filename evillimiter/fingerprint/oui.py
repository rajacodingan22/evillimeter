def lookup_manufacturer(mac):
    if not mac or len(mac) < 8:
        return ""
    oui = mac.upper()[:8]
    if oui in OUI_DATABASE:
        return OUI_DATABASE[oui]
    for manufacturer, pattern_fn in MANUFACTURER_PATTERNS.items():
        if pattern_fn(oui.lower()):
            return manufacturer
    return ""


def guess_device_type(manufacturer, hostname=""):
    if not manufacturer:
        return ""
    if manufacturer in DEVICE_TYPE_MAP:
        return DEVICE_TYPE_MAP[manufacturer](hostname)
    return "Computer"
